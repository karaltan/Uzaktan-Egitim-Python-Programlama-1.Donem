ay = input("Hangi Ay: ")
mevsim = ""
if ay=="aralık" or ay=="ocak" or ay=="şubat":
    mevsim = "kış"
elif ay=="mart" or ay=="nisan" or ay=="mayıs":
    mevsim = "ilk bahar"
elif ay=="haziran" or ay=="temmuz" or ay=="ağustos":
    mevsim = "yaz"
elif ay=="eylül" or ay=="ekim" or ay=="kasım":
    mevsim = "son bahar"
print(mevsim)





